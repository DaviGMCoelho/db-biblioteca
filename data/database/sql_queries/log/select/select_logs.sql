/* BUSCA NA TABELA DE LOGS */
SELECT l.ACAO, p.NOME, p.COD_PERFIL, l.TABELA_AFETADA, l.DESCRICAO, l.ID_ALTERADO, l.MOMENTO_REALIZADO FROM LOGS_ACESSO l
INNER JOIN PERFIS p
	ON P.ID_PERFIL = l.ID_PERFIL
WHERE (ACAO IS NULL OR ACAO LIKE '%'+@ACAO+'%')
	AND (TABELA_AFETADA IS NULL OR TABELA_AFETADA LIKE '%'+@TABELA_AFETADA+'%')
	AND (DESCRICAO IS NULL OR DESCRICACAO LIKE '%'+@DESCRICAO+'%')
	AND (MOMENTO_REALIZADO IS NULL OR MOMENTO_ALTERADO = @MOMENTO_ALTERADO) 