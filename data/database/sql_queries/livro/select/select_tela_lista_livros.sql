/* MOSTRAR LIVROS NO SISTEMA, tela de lista de livros */
SELECT l.NOME, a.AUTOR, l.COD_LIVRO, g.GENERO, l.DATA_PUBLICACAO, COUNT(ex.ID_EXEMPLAR) AS QUANTIDADE, est.ESTADO
	FROM LIVROS l
	INNER JOIN AUTORES a
		ON l.ID_AUTOR = a.ID_AUTOR
	INNER JOIN GENEROS g
		ON l.ID_GENERO = g.ID_GENERO
	INNER JOIN EXEMPLARES ex
		ON l.ID_LIVRO = ex.ID_LIVRO
	INNER JOIN ESTADO_EXEMPLARES est
		ON ex.ID_ESTADO = est.ID_ESTADO
	GROUP BY
		l.NOME, 
		a.AUTOR, 
		l.COD_LIVRO, 
		g.GENERO, 
		l.DATA_PUBLICACAO,
		est.ESTADO