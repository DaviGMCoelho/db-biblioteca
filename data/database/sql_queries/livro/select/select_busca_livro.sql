/* Exibe o livro filtrando pelos dados desejados */
SELECT l.NOME, g.GENERO, l.COD_LIVRO, l.EDICAO, l.DATA_PUBLICACAO, a.AUTOR FROM LIVROS l
INNER JOIN GENEROS g
	ON l.ID_GENERO = g.ID_GENERO
INNER JOIN AUTORES a
	ON l.ID_AUTOR = a.ID_AUTOR
WHERE (@NOME IS NULL OR l.NOME LIKE '%' + @NOME + '%')
	AND (@GENERO IS NULL OR g.GENERO = @GENERO)
	AND (@COD_LIVRO IS NULL OR l.COD_LIVRO = @COD_LIVRO)
	AND (@AUTOR IS NULL OR a.AUTOR = @AUTOR)