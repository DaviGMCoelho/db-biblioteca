/* Mostra todos os emprestimos com status, nome desejado, cod_perfil, cod_livro e nome do livro desejados */
SELECT p.NOME, p.SOBRENOME, p.COD_PERFIL, l.NOME AS LIVRO, l.COD_LIVRO, e.DATA_REGISTRO, e.DATA_DEVOLVER, e.DATA_ENTREGUE, s.STATUS FROM EMPRESTIMOS e
	INNER JOIN STATUS_EMPRESTIMO s
		ON e.ID_STATUS = s.ID_STATUS
	INNER JOIN PERFIS p
		ON e.ID_PERFIL = p.ID_PERFIL
	INNER JOIN EXEMPLARES ex
		ON e.ID_EXEMPLAR = ex.ID_EXEMPLAR
	INNER JOIN LIVROS l
		ON ex.ID_LIVRO = l.ID_LIVRO
	WHERE (@BUSCA IS NULL OR p.NOME LIKE '%' + @BUSCA + '%')
		OR (@BUSCA IS NULL OR p.SOBRENOME LIKE '%' + @BUSCA + '%')
		AND (@COD_PERFIL IS NULL OR p.COD_PERFIL = @COD_PERFIL)
		AND (@STATUS IS NULL OR s.STATUS = @STATUS)
		AND (@COD_LIVRO IS NULL OR l.COD_LIVRO = @COD_LIVRO)
		AND (@NOME_LIVRO IS NULL OR l.NOME_LIVRO = @NOME_LIVRO)

