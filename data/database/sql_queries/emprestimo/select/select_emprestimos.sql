/* Mostra todos os empréstimos no sistema, tela de lista de empréstimos */
SELECT p.NOME AS USUARIO, l.NOME AS LIVRO, p.COD_PERFIL, l.COD_LIVRO, e.DATA_REGISTRO, s.STATUS
	FROM EMPRESTIMOS e
	INNER JOIN STATUS_EMPRESTIMO s
		ON e.ID_STATUS = s.ID_STATUS
	INNER JOIN PERFIS p
		ON e.ID_PERFIL = p.ID_PERFIL
	INNER JOIN EXEMPLARES ex
		ON e.ID_EXEMPLAR = ex.ID_EXEMPLAR
	INNER JOIN LIVROS l
		ON ex.ID_LIVRO = l.ID_LIVRO
