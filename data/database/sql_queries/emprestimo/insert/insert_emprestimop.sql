INSERT INTO EMPRESTIMOS (COD_EMPRESTIMO, DATA_REGISTRO, DATA_DEVOLVER, DATA_ENTREGUE, ID_PERFIL, ID_EXEMPLAR, ID_STATUS)
VALUES(
	@COD_EMPRESTIMO,
	@DATA_REGISTRO,
	@DATA_DEVOLVER,
	@DATA_ENTREGUE,
	(SELECT ID_PERFIL FROM PERFIS WHERE COD_PERFIL = @COD_PERFIL),
	(SELECT TOP 1 ID_EXEMPLAR FROM EXEMPLARES WHERE ID_LIVRO = 
    /* Em ID_ESTADO e ID_STATUS substitua o -- Ativo -- pela constante de estado correspondente */
		(SELECT ID_LIVRO FROM LIVROS WHERE COD_LIVRO = @COD_LIVRO) AND ID_ESTADO = '-- Status: Ativo --'), 
	(SELECT ID_STATUS FROM STATUS_EMPRESTIMO WHERE STATUS = '-- Status: Ativo --')
);