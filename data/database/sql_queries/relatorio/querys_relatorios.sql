
	
/* MOSTRAR VALIDACOES DE DEVOLUÇÃO SISTEMA*/
SELECT l.COD_LIVRO, l.NOME, p.NOME, p.COD_PERFIL,, r.DATAHORA_REQUISICAO
	FROM REQUISICOES r
	INNER JOIN LIVROS l
		ON r.ID_LIVRO = l.ID_LIVRO
	INNER JOIN PERFIS p
		ON p.ID_PERFIL = r.ID_PERFIL
	WHERE r.ID_TIPO = (SELECT ID_TIPO FROM TIPOS_REQUISICAO WHERE REQUISICAO = 'DEVOLUÇÃO')1

/* MOSTRAR VALIDACOES DE EMPRÉSTIMO SISTEMA*/
SELECT l.COD_LIVRO, l.NOME, p.NOME, p.COD_PERFIL, l.DATA_PUBLICACAO, r.DATAHORA_REQUISICAO
	FROM REQUISICOES r
	INNER JOIN LIVROS l
		ON r.ID_LIVRO = l.ID_LIVRO
	INNER JOIN PERFIS p
		ON p.ID_PERFIL = r.ID_PERFIL
	WHERE r.ID_TIPO = (SELECT ID_TIPO FROM TIPOS_REQUISICAO WHERE REQUISICAO = 'EMPRÉSTIMO')