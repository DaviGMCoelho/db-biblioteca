/* Mostra usuários do sistema na tela Bloquear/Desbloquear usuários */
SELECT p.NOME, p.COD_PERFIL, s.STATUS, MAX(e.DATA_REGISTRO) AS ULTIMO_EMPRESTIMO, COUNT(e.ID_PERFIL) AS EMPRESTIMOS
	FROM PERFIS P
	INNER JOIN STATUS_USUARIO s
		ON s.ID_STATUS = p.ID_STATUS
	INNER JOIN EMPRESTIMOS e
		ON e.ID_PERFIL = p.ID_PERFIL
	GROUP BY
		 p.NOME, p.COD_PERFIL, s.STATUS