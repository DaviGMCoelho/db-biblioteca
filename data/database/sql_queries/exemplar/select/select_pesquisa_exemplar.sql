/* Mostra exemplares com base em informações desejadas*/
SELECT l.NOME, l.COD_LIVRO, e.COD_EXEMPLAR, est.ESTADO FROM LIVROS l
INNER JOIN EXEMPLARES e
	ON e.ID_LIVRO = l.ID_LIVRO
INNER JOIN ESTADO_EXEMPLARES est
	ON e.ID_ESTADO = est.ID_ESTADO
WHERE (@NOME IS NULL OR l.NOME LIKE '%' + @NOME + '%')
	AND (@COD_LIVRO IS NULL OR l.COD_LIVRO = @COD_LIVRO)
	AND (@COD_EXEMPLAR IS NULL OR e.COD_EXEMPLAR = @COD_EXEMPLAR)
	AND (@ESTADO IS NULL OR est.ESTADO = @ESTADO)