/* Mostra o perfil com as informações desejadas */
SELECT p.NOME, p.DATA_NASCIMENTO, p.EMAIL, p.COD_PERFIL, t.TIPO, s.STATUS 
FROM PERFIS p
INNER JOIN STATUS_USUARIO s
	ON p.STATUS = s.ID_STATUS
INNER JOIN TIPOS_USUARIO t
	ON p.TIPO_USUARIO = t.ID_TIPO
WHERE (@COD_PERFIL IS NULL OR p.COD_PERFIL = @COD_PERFIL)
	AND (@TIPO IS NULL OR t.TIPO = @TIPO)
	AND (@STATUS IS NULL OR t.TIPO = @STATUS)