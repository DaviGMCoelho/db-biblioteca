DECLARE @ID_PERFIL INT = (SELECT ID_PERFIL FROM PERFIS WHERE COD_PERFIL = @COD_PERFIL)
DECLARE @SALT UNIQUEIDENTIFIER = (SELECT SALT FROM LOGIN_USUARIOS WHERE ID_PERFIL = @ID_PERFIL)

SELECT 1 FROM PERFIS p
INNER JOIN LOGIN_USUARIOS l 
	ON p.ID_PERFIL = l.ID_PERFIL
WHERE COD_PERFIL = @COD_PERFIL
	AND l.SENHA = (HASHBYTES('SHA2_256', CONVERT(VARBINARY, CONVERT(NVARCHAR(36), @SALT) + @SENHA_USUARIO)))